import path from 'path'
require('dotenv').config({ path: path.resolve(__dirname, '../.env') });

import {
  Hex,
  createWalletClient,
  getContract,
  http,
  publicActions,
} from "viem";
import { privateKeyToAccount } from "viem/accounts";
import artifacts from "./submit.artifacts.json";

// Application Binary Interface
const { abi } = artifacts;

const privateKey = process.env.PRIVATE_KEY;
const account = privateKeyToAccount(`0x${privateKey}` as Hex);

(async () => {
  const client = createWalletClient({
    account,
    transport: http(process.env.API_URL),
  }).extend(publicActions);

  const contractAddress = '0x3b26E8DA9aDedAAe86a260b6354aC1855AA65C14';
  const contract = getContract({
    address: contractAddress,
    abi,
    client,
  });

  const tx = await contract.write.recordSubmission([
    'Renz',
    'Guico-Renz',
    '0x6D860cc20cF2De4302E5c7A9cF9C6BFE0dC0f71b',
    '0xf8FAF670f846cA3bE8CC545885fDf17D5D1BCeD9'
    ]);    
  console.log('tx', tx);
})();